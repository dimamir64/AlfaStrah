    public $ProductID;
    
    // Идентификатор статуса полиса. Свойство не являяется обязательным.
    public $DocumentStatusID;
    
    // Данные по страхователю. Свойство не являяется обязательным.
    public $Insurer;
    
    // E-mail клиента. Свойство не являяется обязательным.
    public $Email;
    
    // Дата начала срока страхования. Свойство не являяется обязательным. Допустимые значения: строковые представления дат в формате “yyyy-MM-dd”. При отсутствии свойства или указании недопустимого значения считается, что было передано ”1900-01-01”.
    public $EffectiveDate;
    
    // Дата окончания срока страхования. Свойство не являяется обязательным. Допустимые значения: строковые представления дат в формате “yyyy-MM-dd”. При отсутствии свойства или указании недопустимого значения считается, что было передано ”1900-01-01”.
    public $ExpirationDate;
    
    // Количество дней. Свойство являяется обязательным.
    public $Duration;
    
    // Количество добавленных к сроку страхования високосных дней (для многократных поездок). Свойство не являяется обязательным.
    public $AdditionalLeapDays;
    
    // Идентификатор валюты страхования из справочника. Свойство являяется обязательным.
    public $AmountCurrencyCd;
    
    // Массив рисков. Свойство являяется обязательным при акцептации. Если какой-либо из рисков отсутствует, то передавать его в массиве не нужно.
    public $Risks;
    
    // Список идентификаторов стран выезда из справочника, разделенных запятыми. Свойство является обязательным.
    public $Countries;
    
    // Дополнительные условия (спорт, профессия, иное). Свойство не является обязательным. Аналогично рискам, если дополнительное условие не включено в полис, то оно не заполняется (если они отсутствуют, то ничего не передается).
    public $ExtraConditions;
    
    // Список застрахованных лиц. Свойство не является обязательным.
    public $Insured;
    
    // Дата выписки полиса. Свойство не являяется обязательным. Свойство может присутствовать только в возвращаемых объектах и должно отсутствовать в передаваемых.
    public $DocumentDate;
    
    // Общая страховая премия по полису в валюте. Свойство не являяется обязательным. Свойство может присутствовать только в возвращаемых объектах и должно отсутствовать в передаваемых. Заполняется при расчете сервисом Вирту.
    public $CurrencyPremium;
    
    // Общая страховая премия по полису в рублях. Свойство не являяется обязательным. Свойство может присутствовать только в возвращаемых объектах и должно отсутствовать в передаваемых. Заполняется при расчете сервисом Вирту.
    public $RurPremium;
    
    // Дата, на которую должен быть применен курс валют (клиентская дата). Свойство не являяется обязательным. Допустимые значения: строковые представления дат в формате “yyyy-MM-dd”. При отсутствии свойства или указании недопустимого значения считается, что была передана текущая дата (на сервере).
    public $CurrencyRatesDate;
    
    // Признак, что полис выписан с сайта (true - если с сайта). Свойство не являяется обязательным. При отсутствии свойства считается, что было передано значение false.
    public $SiteFlag;
}
