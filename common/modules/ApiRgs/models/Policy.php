<?php

namespace common\modules\ApiRgs\models;

class Policy {
    // Идентификатор полиса в системе Вирту. Свойство не являяется обязательным. Если отсутствует, то создается новый полис (при сохранении и акцепте).
    public $ID;

    // Код СКК – в зависимости от региона. Свойство не является обязательным.
    public $SKKCode;
    
    // Филиал РГС. Свойство не является обязательным.
    public $FilialName;
    
