    public $AuthCode;
    
    // Номер заказа для платежного шлюза. Свойство не является обязательным.
    public $orderId;
    
    // Маркер прямого страхования. Свойство не являяется обязательным (должно отсутствовать при оформлении полисов ВЗР через Альфа-клик).
    public $DirectMarker;
    
    // Серия полиса. Свойство не являяется обязательным. Генерируется при акцептации.
    public $Serial;
    
